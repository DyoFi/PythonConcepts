import turtle


t=turtle.Turtle()
t.up()
t.goto(0,0)
t.down()

t.dot(10)

t.up()
t.goto(40,0)
t.down()
t.dot(10)

t.up()
t.goto(-20,-40)
t.down()
t.forward(10)
t.right(90)
t.forward(5)
t.left(90)
t.forward(60)
t.left(90)
t.forward(5)
t.right(90)
t.forward(10)

turtle.done()